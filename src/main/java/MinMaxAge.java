package main.java;

public class MinMaxAge {

	int min;
	int max;
	
	@Override
	public String toString() {
		return "MinMaxAge [min=" + min + ", max=" + max + "]";
	}
	public int getMin() {
		return min;
	}
	public void setMin(int min) {
		this.min = min;
	}
	public int getMax() {
		return max;
	}
	public void setMax(int max) {
		this.max = max;
	}
}


